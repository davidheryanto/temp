#pragma mark - Table view data source

- (NSInteger)numberOfSectionsInTableView:(UITableView *)tableView {
    return 1;
}

- (NSInteger)tableView:(UITableView *)tableView numberOfRowsInSection:(NSInteger)section {
    return [self.contentArrays count];
}

- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"cell" forIndexPath:indexPath];
    
    // Configure the cell...
    UILabel *title = (UILabel *)[cell viewWithTag:TAG_TITLE];
    UILabel *subtitle = (UILabel *)[cell viewWithTag:TAG_SUBTITLE];
    subtitle.textColor = [UIColor greenColor];
    UIImageView *imageView = (UIImageView *)[cell viewWithTag:TAG_IMAGE];
    
    title.text = [self.contentArrays objectAtIndex:indexPath.row];
    subtitle.text = [[self.contentArrays objectAtIndex:indexPath.row] stringByAppendingString:@" sub sub"];
    
    
    return cell;

}

